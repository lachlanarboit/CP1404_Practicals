import random
print(random.randint(5, 20))  # line 1 - Lowest number is 5 and highest number is 20
print(random.randrange(3, 10, 2))  # line 2 - Lowest number is 3 and highest number is 9 (Because 10 is not able to be achieved when working in steps of 2)
print(random.uniform(2.5, 5.5))  # line 3 - Lowest number is 2.5 and highest number is 5.5
print(random.randint(1, 100))   # line 4 - Lowest number is 1 and highest number is 100